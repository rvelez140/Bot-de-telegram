@app.route('/register', methods=['GET', 'POST'])
def register():
    """Registro de usuario"""
    if request.method == 'POST':
        username = request.form.get('username', '').strip()
        password = request.form.get('password', '')
        confirm_password = request.form.get('confirm_password', '')

        # Validaciones
        if not username or not password:
            return render_template('register.html', error='Usuario y contraseña son requeridos')

        if len(username) < 3:
            return render_template('register.html', error='El usuario debe tener al menos 3 caracteres')

        if len(password) < 6:
            return render_template('register.html', error='La contraseña debe tener al menos 6 caracteres')

        if password != confirm_password:
            return render_template('register.html', error='Las contraseñas no coinciden')

        # Crear usuario
        try:
            conn = get_db()
            cursor = conn.cursor()
            password_hash = generate_password_hash(password)
            cursor.execute('INSERT INTO web_users (username, password_hash) VALUES (?, ?)',
                          (username, password_hash))
            conn.commit()
            conn.close()

            return redirect(url_for('login', registered=True))

        except sqlite3.IntegrityError:
            return render_template('register.html', error='El usuario ya existe')

    return render_template('register.html')

@app.route('/login', methods=['GET', 'POST'])
def login():
    """Login de usuario"""
    if request.method == 'POST':
        username = request.form.get('username', '').strip()
        password = request.form.get('password', '')

        conn = get_db()
        cursor = conn.cursor()
        user = cursor.execute('SELECT * FROM web_users WHERE username = ?', (username,)).fetchone()
        conn.close()

        if user and check_password_hash(user['password_hash'], password):
            session['user_id'] = user['id']
            session['username'] = user['username']
            session.permanent = True
            return redirect(url_for('dashboard'))
        else:
            return render_template('login.html', error='Usuario o contraseña incorrectos')

    registered = request.args.get('registered', False)
    return render_template('login.html', registered=registered)

@app.route('/logout')
def logout():
    """Cerrar sesión"""
