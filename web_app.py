@app.route('/download', methods=['POST'])
@login_required
def download():
    """Descargar video desde la interfaz web"""
    user_id = session['user_id']
    url = request.form.get('url', '').strip()

    if not url:
        return jsonify({'success': False, 'error': 'URL es requerida'})

    # Detectar plataforma
    platform = get_platform(url)
    if not platform:
        return jsonify({'success': False, 'error': 'URL no reconocida o plataforma no soportada'})

    # Obtener cookies si existen para esta plataforma
    cookies_str = None
    if platform == 'twitter':
        conn = get_db()
        cursor = conn.cursor()
        account = cursor.execute(
            'SELECT cookies FROM social_accounts WHERE user_id = ? AND platform = ?',
            (user_id, 'twitter')
        ).fetchone()
        conn.close()

        if account:
            cookies_str = account['cookies']

    # Descargar video
    success, filepath, error_msg, is_image = download_video(url, user_id, cookies_str)

    if not success:
        return jsonify({'success': False, 'error': f'Error al descargar: {error_msg}'})

    # Guardar en historial
    try:
        conn = get_db()
        cursor = conn.cursor()
        cursor.execute('''
            INSERT INTO download_history (user_id, url, platform, filename)
            VALUES (?, ?, ?, ?)
        ''', (user_id, url, platform, os.path.basename(filepath)))
        conn.commit()
        conn.close()
    except:
        pass  # No es crítico si falla el historial

    # Retornar archivo para descarga
    try:
        return send_file(
            filepath,
            as_attachment=True,
            download_name=os.path.basename(filepath)
        )
    finally:
        # Limpiar archivo después de 1 minuto
        import threading
        def cleanup():
            import time
            time.sleep(60)
            try:
                os.remove(filepath)
            except:
                pass

        threading.Thread(target=cleanup).start()

