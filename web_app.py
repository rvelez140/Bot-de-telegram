# Crear directorio de descargas si no existe
os.makedirs(DOWNLOAD_DIR, exist_ok=True)

# ===== Base de datos =====

def init_db():
    """Inicializar base de datos"""
    conn = sqlite3.connect(DATABASE_PATH)
    cursor = conn.cursor()

    # Tabla de usuarios web
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS web_users (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            username TEXT UNIQUE NOT NULL,
            password_hash TEXT NOT NULL,
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
        )
    ''')

    # Tabla de cuentas de redes sociales
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS social_accounts (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            user_id INTEGER NOT NULL,
            platform TEXT NOT NULL,
            platform_username TEXT NOT NULL,
            cookies TEXT,
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
            updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
            FOREIGN KEY (user_id) REFERENCES web_users (id),
            UNIQUE(user_id, platform)
        )
    ''')

    # Tabla de historial de descargas
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS download_history (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            user_id INTEGER NOT NULL,
            url TEXT NOT NULL,
            platform TEXT NOT NULL,
            filename TEXT,
            downloaded_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
            FOREIGN KEY (user_id) REFERENCES web_users (id)
        )
    ''')

    conn.commit()
    conn.close()

def get_db():
    """Obtener conexión a la base de datos"""
    conn = sqlite3.connect(DATABASE_PATH)
    conn.row_factory = sqlite3.Row
    return conn

# ===== Decoradores =====

def login_required(f):
    """Decorador para requerir autenticación"""
    @wraps(f)
    def decorated_function(*args, **kwargs):
        if 'user_id' not in session:
            return redirect(url_for('login'))
        return f(*args, **kwargs)
    return decorated_function

# ===== Utilidades de Twitter/X =====

async def generate_twitter_cookies(username, password):
    """
    Genera cookies de Twitter usando Playwright
    Retorna: (success: bool, cookies_str: str, error_msg: str)
    """
    try:
        async with async_playwright() as p:
            browser = await p.chromium.launch(headless=True)
            context = await browser.new_context()
            page = await context.new_page()

            # Navegar a la página de login
            await page.goto('https://twitter.com/i/flow/login', timeout=60000)
            await page.wait_for_timeout(3000)

            # Ingresar usuario
            username_input = page.locator('input[autocomplete="username"]')
            await username_input.fill(username)
            await page.wait_for_timeout(1000)

            # Click en "Next"
            next_button = page.locator('button:has-text("Next"), button:has-text("Siguiente")')
            await next_button.click()
            await page.wait_for_timeout(3000)

            # Ingresar contraseña
            password_input = page.locator('input[name="password"]')
            await password_input.fill(password)
            await page.wait_for_timeout(1000)

            # Click en "Log in"
            login_button = page.locator('button[data-testid="LoginForm_Login_Button"]')
            await login_button.click()

            # Esperar a que cargue la página principal
            try:
                await page.wait_for_url('https://twitter.com/home', timeout=30000)
            except:
                # Intentar con x.com
                await page.wait_for_url('https://x.com/home', timeout=30000)

            # Obtener cookies
            cookies = await context.cookies()

            # Convertir a formato Netscape
            cookies_str = "# Netscape HTTP Cookie File\n"
            cookies_str += "# This file is generated by Playwright. Do not edit.\n\n"

            for cookie in cookies:
                domain = cookie.get('domain', '')
                flag = 'TRUE' if domain.startswith('.') else 'FALSE'
                path = cookie.get('path', '/')
                secure = 'TRUE' if cookie.get('secure', False) else 'FALSE'
                expiration = str(int(cookie.get('expires', -1)))
                name = cookie.get('name', '')
                value = cookie.get('value', '')

                cookies_str += f"{domain}\t{flag}\t{path}\t{secure}\t{expiration}\t{name}\t{value}\n"

            await browser.close()
            return True, cookies_str, None

    except Exception as e:
        return False, None, str(e)

# ===== Utilidades de descarga =====

def get_platform(url):
    """Detectar plataforma de la URL"""
    patterns = {
        'tiktok': r'(tiktok\.com|vm\.tiktok\.com|vt\.tiktok\.com)',
        'youtube': r'(youtube\.com|youtu\.be)',
        'twitter': r'(twitter\.com|x\.com)',
        'instagram': r'instagram\.com'
    }

    for platform, pattern in patterns.items():
        if re.search(pattern, url):
            return platform

    return None

def download_video(url, user_id, cookies_str=None):
    """
    Descargar video usando yt-dlp
    Retorna: (success: bool, filepath: str, error_msg: str, is_image: bool)
    """
    try:
        # Configuración de yt-dlp
        ydl_opts = {
            'format': 'bestvideo[ext=mp4]+bestaudio[ext=m4a]/best[ext=mp4]/best',
            'outtmpl': os.path.join(DOWNLOAD_DIR, f'download_{user_id}_%(id)s.%(ext)s'),
            'quiet': True,
            'no_warnings': True,
        }

        # Usar cookies si están disponibles
        if cookies_str:
            cookies_file = os.path.join(DOWNLOAD_DIR, f'cookies_{user_id}.txt')
            with open(cookies_file, 'w') as f:
                f.write(cookies_str)
            ydl_opts['cookiefile'] = cookies_file

        # Descargar
        with yt_dlp.YoutubeDL(ydl_opts) as ydl:
            info = ydl.extract_info(url, download=True)
            filename = ydl.prepare_filename(info)

            # Verificar si es imagen
            is_image = filename.lower().endswith(('.jpg', '.jpeg', '.png', '.webp'))

            # Limpiar archivo de cookies temporal
            if cookies_str:
                try:
                    os.remove(cookies_file)
                except:
                    pass

            return True, filename, None, is_image

    except Exception as e:
        return False, None, str(e), False

